
//Teacher1,Teacher2,Teacher3 - all three came to the class and individually asked you to write the word "John" on the board.Use nested for loop.
class NestedFor1
{
    public static void main(String args[])
        {
            for (int teacher=1;teacher<=3;teacher++)
            {
                for (int i=1;i<=5;i++)
                {
                    System.out.println("John");
                }
            }
        }
}
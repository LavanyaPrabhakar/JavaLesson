// Use And operator and Compare two boolean values
import java.util.Scanner;
class LogicalOper
{
public static void main(String args[])
{

// both true means true
//either one is false means false
//both false means false

    boolean icecream = true;
    boolean hungry = true;
    
    if (icecream && hungry)
    {
        System.out.println("Eat");
    }
    else
    {
        System.out.println("Don't Eat");
    }
}
}
